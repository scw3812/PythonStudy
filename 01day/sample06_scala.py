print(int(3.14))
print(int(-3.14))
print(int("3"))
print(int(True))
print(int(False))

print(float(3))
print(float(-3))
print(float("3"))
print(float(True))
print(float(False))

print(str(3.14))
print(str(-3.14))
print(str(3))
print(str(True))
print(str(False))

print(bool(1.0))
print(bool(0.0))
print(bool("aaa"))  # True
print(bool(""))  # False
print(bool(None))  # False
print(bool([]))  # False
print(bool({}))  # False
print(bool(()))  # False
print(bool(1))

